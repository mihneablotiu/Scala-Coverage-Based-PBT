package benchmark

object RealWorld {

  // Models integer parsing with whitespace, sign, digits, and overflow.
  // Expected: no tactic should reliably dominate because the hard checks depend on character sequences, not mined String literals or structural list/tree mutations.
  def atoi(s: String): Int = {
    var i = 0
    val n = s.length
    while (i < n && s(i) == ' ') i += 1
    var sign = 1
    if (i < n && (s(i) == '+' || s(i) == '-')) {
      if (s(i) == '-') sign = -1
      i += 1
    }
    var result = 0L
    while (i < n && s(i) >= '0' && s(i) <= '9') {
      result = result * 10 + (s(i) - '0')
      if (sign == 1 && result > Int.MaxValue) return Int.MaxValue
      if (sign == -1 && -result < Int.MinValue) return Int.MinValue
      i += 1
    }
    (sign * result).toInt
  }

  // Models decimal/scientific-number validation.
  // Expected: no tactic should reliably dominate because branches such as `c == '.'` or `c == 'e'` use Char literals that the pool does not mine.
  def isValidNumber(s: String): Boolean = {
    var i         = 0
    val n         = s.length
    var seenDigit = false
    var seenDot   = false
    var seenExp   = false
    while (i < n) {
      val c = s(i)
      if (c >= '0' && c <= '9') seenDigit = true
      else if (c == '+' || c == '-') {
        if (i > 0 && s(i - 1) != 'e' && s(i - 1) != 'E') return false
      } else if (c == '.') {
        if (seenDot || seenExp) return false
        seenDot = true
      } else if (c == 'e' || c == 'E') {
        if (seenExp || !seenDigit) return false
        seenExp = true
        seenDigit = false
      } else return false
      i += 1
    }
    seenDigit
  }

  // Models Roman numeral conversion with subtractive pairs.
  // Expected: no tactic should reliably dominate because the meaningful inputs are character patterns like `I` before `V`, not values generated by the current pool.
  def romanToInt(s: String): Int = {
    var total = 0
    var i     = 0
    while (i < s.length) {
      val cur = s(i) match {
        case 'I' => 1
        case 'V' => 5
        case 'X' => 10
        case 'L' => 50
        case 'C' => 100
        case 'D' => 500
        case 'M' => 1000
        case _   => 0
      }
      val nxt =
        if (i + 1 < s.length)
          s(i + 1) match {
            case 'I' => 1
            case 'V' => 5
            case 'X' => 10
            case 'L' => 50
            case 'C' => 100
            case 'D' => 500
            case 'M' => 1000
            case _   => 0
          }
        else 0
      if (cur < nxt) total -= cur else total += cur
      i += 1
    }
    total
  }

  // Models dotted version comparison.
  // Expected: no tactic should reliably dominate because useful inputs require coordinated digit-and-dot strings, while mutation only edits strings locally.
  def compareVersion(v1: String, v2: String): Int = {
    val a = v1.split('.')
    val b = v2.split('.')
    var i = 0
    val n = math.max(a.length, b.length)
    while (i < n) {
      var x = 0L
      if (i < a.length && a(i).nonEmpty && a(i).forall(c => c >= '0' && c <= '9')) {
        var j = 0
        while (j < a(i).length) {
          x = (x * 10 + (a(i)(j) - '0')).min(Int.MaxValue.toLong + 1L)
          j += 1
        }
      }

      var y = 0L
      if (i < b.length && b(i).nonEmpty && b(i).forall(c => c >= '0' && c <= '9')) {
        var j = 0
        while (j < b(i).length) {
          y = (y * 10 + (b(i)(j) - '0')).min(Int.MaxValue.toLong + 1L)
          j += 1
        }
      }

      if (x < y) return -1
      if (x > y) return 1
      i += 1
    }
    0
  }

  // Models IPv4 dotted-quad validation.
  // Expected: no tactic should reliably dominate because valid addresses require several coordinated numeric string segments, not just one mined literal.
  def isValidIPv4(s: String): Boolean = {
    val parts = s.split('.')
    if (parts.length != 4) return false
    var i = 0
    while (i < 4) {
      val p = parts(i)
      if (p.isEmpty || p.length > 3) return false
      if (!p.forall(c => c >= '0' && c <= '9')) return false
      if (p.length > 1 && p(0) == '0') return false
      if (p.toInt > 255) return false
      i += 1
    }
    true
  }

  // Models Gregorian date validation.
  // Expected: pool/pool-mutation may help modestly because tuple pooling can reuse mined bounds like `1`, `12`, `28`, `29`, `30`, and `31`.
  def isValidDate(year: Int, month: Int, day: Int): Boolean =
    if (year < 1 || year > 9999) false
    else if (month < 1 || month > 12) false
    else {
      val leap = (year % 4 == 0 && year % 100 != 0) || (year % 400 == 0)
      val days = month match {
        case 1 | 3 | 5 | 7 | 8 | 10 | 12 => 31
        case 4 | 6 | 9 | 11              => 30
        case 2                           => if (leap) 29 else 28
        case _                           => 0
      }
      day >= 1 && day <= days
    }

  // Models Luhn checksum validation.
  // Expected: no tactic should reliably dominate because satisfying `sum % 10 == 0` needs checksum-aware digit construction, which no current tactic implements.
  def luhn(s: String): Boolean = {
    if (s.isEmpty) return false
    var sum = 0
    var alt = false
    var i   = s.length - 1
    while (i >= 0) {
      val c = s(i)
      if (c < '0' || c > '9') return false
      var d = c - '0'
      if (alt) {
        d *= 2
        if (d > 9) d -= 9
      }
      sum += d
      alt = !alt
      i -= 1
    }
    sum % 10 == 0
  }
}
